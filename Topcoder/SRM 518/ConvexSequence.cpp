class ConvexSequence {
    public:
    long long getMinimum(vector<int> a) {
        ll ans = 0;
        while(1){
            bool found = false;
            for(int i = 1; i < sz(a)-1; i++){
                if(a[i]*2 > a[i-1]+a[i+1]) {
                    ans += a[i] - (a[i-1]+a[i+1]) / 2;
                    a[i] = (a[i-1] + a[i+1]) / 2;
                    found = true;
                }
            }
            if(!found) break;
        }
        return ans;
    }
};

