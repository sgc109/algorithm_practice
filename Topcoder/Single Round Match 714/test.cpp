map<long long, long long> dp;

long long hashing(string s){
    long long ret = 0;
    for(int i = 0 ; i < s.size(); i++){
        if(s[i] == '(') ret++;
        ret = (ret * 2) % 1000000000000000007;
    }
    return ret;
}

