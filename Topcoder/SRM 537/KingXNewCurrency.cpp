int exist[203];
class KingXNewCurrency {
    public:
    int howMany(int A, int B, int X) {
        int ans = 0;
        if(A % X == 0 && B % X == 0) return -1;
        for(int Y = 1; Y <= 200; Y++){
            memset(exist,0,sizeof(exist));
            exist[0] = 1;
            for(int i = 0; i <= 200; i++){
                if(!exist[i]) continue;
                if(i + X <= 200) exist[i + X] = 1;
                if(i + Y <= 200) exist[i + Y] = 1;
            }
            if(exist[A] && exist[B]) ans++;
        }
        return ans;
    }
};

