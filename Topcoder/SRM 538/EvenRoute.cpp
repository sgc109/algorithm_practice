int odd, even;
class EvenRoute {
    public:
    string isItPossible(vector<int> x, vector<int> y, int wantedParity) {
        odd = even = 0;
        int n = (int)x.size();
        for(int i = 0 ; i < n; i++){
            int p = (x[i] + y[i] + 50000000) % 2;
            if(p & 1) odd = 1;
            else even = 1;
        }
        if(odd && even) return "CAN";
        if(odd) return wantedParity == 1 ? "CAN" : "CANNOT";
        return wantedParity == 0 ? "CAN" : "CANNOT";
    }
};

